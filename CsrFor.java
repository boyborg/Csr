public class CsrFor{
    public static void main(String[] args) {
        
        for(int i=0; i<5; i++){
            System.out.println(i);
        }

        int u =0;
        
        while(u<=10){
            System.out.println(u);
            u++;
        }

        System.out.println("\n");
        int j=0;
        do{
            System.out.println(j);
            j++;
        }while(j<=5);
    }
}